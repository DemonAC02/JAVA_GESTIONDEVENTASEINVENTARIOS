   private void RegistrarCliente() {
    String nombreCli = txtNombreVenta.getText().trim();
    int dniCli;

    // Validar que se haya ingresado un DNI válido
    String dniText = txtDniVenta.getText().trim();
    if (dniText.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Ingrese el DNI del cliente", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    try {
        dniCli = Integer.parseInt(dniText);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "El DNI debe ser un número válido", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Verificar si ya existe un cliente con el mismo DNI y nombre
    for (Cliente cliente : VRep.listarClientes()) {
        if (cliente.getDniCli() == dniCli && cliente.getNombreCli().equals(nombreCli)) {
            JOptionPane.showMessageDialog(null, "Ya existe un cliente con el mismo DNI y nombre", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }

    // Agregar el nuevo cliente
    Cliente newCli = new Cliente(dniCli, nombreCli);
    VRep.RegistarCliente(newCli);
}
