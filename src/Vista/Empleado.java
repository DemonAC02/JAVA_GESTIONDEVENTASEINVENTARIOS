    private void txtCantidadVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCantidadVentaActionPerformed
        // TODO add your handling code here:
        if (!"".equals(txtCantidadVenta.getText())) {
                int id = Integer.parseInt(txtCodigovnt.getText());
                String descripcion = txtNombreVnt.getText();
                int cant = Integer.parseInt(txtCantidadVenta.getText());
                double precio = Double.parseDouble(txtPreciovnt.getText());
                double total = cant * precio;   
                int stock = Integer.parseInt(txtStockVenta.getText());
                if (stock >= cant) {
                    item = item + 1;
                    tmp = (DefaultTableModel) tablaVenta.getModel();
                    for (int i = 0; i < tablaVenta.getRowCount(); i++) {
                        if (tablaVenta.getValueAt(i, 1).equals(txtNombreVnt.getText())) {
                            JOptionPane.showMessageDialog(null, "El producto ya esta registrado");
                            return;
                        }
                    }
                    ArrayList lista = new ArrayList();
                    lista.add(item);
                    lista.add(id);
                    lista.add(descripcion);
                    lista.add(cant);
                    lista.add(precio);
                    lista.add(total);
                    Object[] O = new Object[5];
                    O[0] = lista.get(1);
                    O[1] = lista.get(2);
                    O[2] = lista.get(3);
                    O[3] = lista.get(4);
                    O[4] = lista.get(5);
                    tmp.addRow(O);
                    tablaVenta.setModel(tmp);
                    TotalPagar();
                    LimparVenta();
                } else {
                    JOptionPane.showMessageDialog(null, "Stock no disponible");
                }
        }
    }//GEN-LAST:event_txtCantidadVentaActionPerformed

