    private void btnAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarProductoActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(null, "¿Estás seguro de que deseas agregar el producto?", "Confirmación", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        String NomPro = txtNuevoNombreProducto.getText();
        String PrecProText = txtNuevoPrecioProducto.getText();
        String StoProText = spnStockNuevo.getValue().toString();

        if (NomPro.isEmpty() || PrecProText.isEmpty() || StoProText.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos");
        } else {
            try {
                Double PrecPro = Double.valueOf(PrecProText);
                Integer StoPro = Integer.valueOf(StoProText);
                Producto proD = new Producto(NomPro, PrecPro, StoPro);
                PRep.agregarProducto(proD);
                LimpiarTable();
                ListarProductos();
                LimpiarProductos();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Por favor, ingrese valores numéricos válidos para el precio y el stock");
            }
        }
  
}

    }//GEN-LAST:event_btnAgregarProductoActionPerformed

