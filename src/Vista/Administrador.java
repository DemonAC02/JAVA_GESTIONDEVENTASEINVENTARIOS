   private void RegistrarVenta() {
    int dnicliente;
    String nomcliente;
    String dniText = txtDniVenta.getText().trim();
    if (dniText.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Ingrese el DNI del cliente", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    try {
        dnicliente = Integer.parseInt(dniText);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "El DNI debe ser un número válido", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    nomcliente = txtNombreVenta.getText().trim();
    if (nomcliente.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Ingrese el nombre del cliente", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    double monto = Totalpagar;
    if (monto == 0) {
        JOptionPane.showMessageDialog(null, "Venta invalida", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    

    ven.setDniCompr(dnicliente);
    ven.setNombreCompr(nomcliente);
    ven.setMonto(monto);
    ven.setIdMetodoPago(cmbPago.getSelectedIndex());
    ven.setFechaVenta(fechaActual);
    VRep.agregarVenta(ven);
}


    
